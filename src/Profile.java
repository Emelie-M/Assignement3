public class Profile {
    String username;
    String password;
    String hint1;
    String ans_hint1;
    String hint2;
    String ans_hint2;

    public Profile(String username, String password, String hint1, String ans_hint1, String hint2, String ans_hint2) {
        this.username = username;
        this.password = password;
        this.hint1 = hint1;
        this.ans_hint1 = ans_hint1;
        this.hint2 = hint2;
        this.ans_hint2 = ans_hint2;
    }
}
